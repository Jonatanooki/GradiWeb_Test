class Product {

    constructor(id, title, tags, totalInventory, tracksInventory, featuredImage, prices) {
        this._id = id;
        this._title = title;
        this._tags = tags;
        this._totalInventory = totalInventory;
        this._tracksInventory = tracksInventory;
        this._featuredImage = featuredImage;
        this._prices = prices;
    }
}